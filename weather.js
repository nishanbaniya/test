weather=prompt("How is the weather?")

if(weather =="rainy"){
    console.log("Take an Umbrella");
    alert("Take an Umbrella");
}
else if(weather =="sunny"){
    console.log("Wear a Sunglasses");
    alert("Wear a Sunglasses");
}
else{
    console.log("Stay in house");
    alert("Stay in house");
}